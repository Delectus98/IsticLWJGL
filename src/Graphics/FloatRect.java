package Graphics;

/**
 * FloatRect represents coordinates and dimension of a rectangle
 */
public class FloatRect {
    public float l, t, w, h;

    /**
     *
     * @param l left coordinate
     * @param t top coordinate
     * @param w width
     * @param h height
     */
    public FloatRect(float l, float t, float w, float h) {
        this.l = l;
        this.t = t;
        this.w = w;
        this.h = h;
    }

}
